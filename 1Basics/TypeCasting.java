import java.util.Scanner;
public class TypeCasting {
public static void main (String args[]){
char x = 'a';
char y = 'b';
System.out.println((int)(x)); // converting x into int
System.out.println((int)(y)); // converting y into int
System.out.println(x-y);  // Typcasting and x and y are now converted into int
}
     
}
